package be.com.generationObjeto;

public class Paciente {
	String nome, doença;
	boolean grave;
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getDoença() {
		return doença;
	}
	public void setDoença(String doença) {
		this.doença = doença;
	}
	public boolean isGrave() {
		return grave;
	}
	public void setGrave(boolean grave) {
		this.grave = grave;
	}
	
	

}
