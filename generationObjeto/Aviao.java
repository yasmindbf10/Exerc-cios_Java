package be.com.generationObjeto;

public class Aviao {
private String modelo, destino;
private boolean partir;

public boolean isPartir() {
	return partir;
}

public void setPartir(boolean partir) {
	this.partir = partir;
}

public String getModelo() {
	return modelo;
}

public void setModelo(String modelo) {
	this.modelo = modelo;
}

public String getDestino() {
	return destino;
}

public void setDestino(String destino) {
	this.destino = destino;
}


}
